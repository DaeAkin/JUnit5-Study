package dev.donghyeon.junitstudy;

public class Calculator {
    public int add(int i, int i1) {
        return i+i1;
    }

    public int multiply(int i, int i1) {
        return i * i1;
    }

    public int divide(int i, int i1) {
        return i/i1;
    }

    public int subtract(int i, int i1) {
        return i-i1;
    }
}
